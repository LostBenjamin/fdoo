from openerp.osv import fields,osv

class sale_order(osv.osv):
    
    def _createdinv_rate(self, cursor, user, ids, name, arg, context=None):
        res = {}
        for sale in self.browse(cursor, user, ids, context=context):
            if sale.invoiced:
                res[sale.id] = 100.0
                continue
            tot = 0.0
            for invoice in sale.invoice_ids:
                if invoice.state != "cancel":
                    tot += invoice.amount_untaxed
            if tot:
                res[sale.id] = min(100.0, tot * 100.0 / (sale.amount_untaxed or 1.00))
            else:
                res[sale.id] = 0.0
        return res
    
    _inherit = "sale.order"
    _columns = {
        "createdinv_rate" : fields.function(_createdinv_rate, string="Created Invoices", type="float")
    }
sale_order()
